package game.map;

public class Pourcentage {
	int id;
	int prob;
	
	public Pourcentage(int id, int prob){
		this.id = id;
		this.prob = prob;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getProb() {
		return prob;
	}

	public void setProb(int prob) {
		this.prob = prob;
	}
	
	

}
